package org.jbpm.formbuilder.shared.rep.trans;

public class LanguageException extends Exception {

    private static final long serialVersionUID = -407925898649708362L;

    public LanguageException() {
        super();
    }

    public LanguageException(String message, Throwable cause) {
        super(message, cause);
    }

    public LanguageException(String message) {
        super(message);
    }

    public LanguageException(Throwable cause) {
        super(cause);
    }
}
